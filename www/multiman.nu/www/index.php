        Om mig
    </h2>
    <div class="separator"></div>
    <p id="section-about-me-text">
        Vem är jag då som person?<br/>
        Jag anses vara en nyfiken entreprenör, socialt kompetent med uttalat säljande driv.<br/>
        I arbetsledande ställning har jag organiserat utställningar, hållit grundläggande CRM-utbildningar,<br/>
        utarbetat handlingsplaner samt vitaliserat olika försäljningskanaler.<br/>
        Viktigt för mig är affärsmässighet, kundorienterad hög servicenivå, kompetens och flexibilitet.<br/>
        <br/>
        Varför då detta ?<br/>
        Marknadsföring, försäljning och service är det jag brinner för.<br/>
        Jag har ambitionen att mina kunder skall känna att jag är deras prioriterade samarbetspartner.<br/>
        Tidigare uppdrag har omfattat just dessa områden med ett mycket gott resultat, referenser förmedlas på begäran.<br/>
        <br/>
        I mitt eget företag ”MULTIMAN” har en mindre bilverkstadsverksamhet bedrivits på hobbybasis med inriktning på plåt, lack och motorarbeten.<br/>
        <br/>
        Mina fritidsintressen utöver min familj handlar oftast om bilarna, motorbåten och motorcyklarna.<br/>
        Är idag sekreterare och styrelseledamot i Classic Honda Club Sweden med ca 1400 medlemmar.<br/>
        I föreningsvärlden har jag tidigare ingått i tävlingsledningen för Järfälla MK (GoKart).<br/>
        För Kanaans Simsällskap arbetat som styrelseledamot, badhallsansvarig, sammankallande i valberedningen och utbildningsansvarig.<br/>
        <br/>
        En av mina starkaste egenskaper är att bygga långsiktiga relationer samt vara en god arbetskamrat.<br/>
        Jag talar och skriver engelska som ett naturligt andraspråk.<br/>
        Min datorkompetens omfattar bl a Office-paketet och olika CRM-system.<br/>
